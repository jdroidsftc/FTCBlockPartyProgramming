/////////////////////////////////////////////////////////////////////////////////////////////////////
// Task: Rack
// Allows for Rack to move up and down, runs parallely along with the drive and other tasks
// Raise the Rack based on the predefined encoder value.
// TopHat 0:
// 		Moves Rack Up;
// TopHat 4:
// 		Moves Rack Down
// Left and Right bumper
// 		Stops the Rack from climbing up or down.
// TopHat 6 (alternate way to make rack go up regardless of encoder logic)
//    Rack goes up and no encoder logic, releasing stops
// TopHat 2 (alternate way to make rack go down regardless of encoder logic)
//    Rack goes down and no encoder logic, releasing stops
/////////////////////////////////////////////////////////////////////////////////////////////////////
task RackNotUsedAsEncoderWasRemoved()
{
	while(true)
	{
		int RackTicks;
		getJoystickSettings(joystick);
		if (joystick.joy2_TopHat == 0 ) //Rack Up
		{
			//initialize the encoder
			nMotorEncoder[motorHang] = 0;

			//set the target value based on the maximum value and the ticks the rack has gone up
			nMotorEncoderTarget[motorHang] = MAX_ENCODER_HANG - currentRackTicks;

			//power the motor
			motor[motorHang] = 70;

			//wait for the encoder target to be reached, or the stop button is pressed
			while( (nMotorRunState[motorHang] != runStateIdle) && (joy2Btn(6) != 1)  && (joy2Btn(5) != 1) ) //Right/left bumper is an emergency stop
			{
				//if encoder target is not met, or the emergency stop button is not pressed, continue
				getJoystickSettings(joystick);

				//read the number ticks the rack went up
				RackTicks = nMotorEncoder[motorHang];
			}

			//encoder target is reached or the emergency stop is pressed, reset the motor power to stop
			motor[motorHang] = 0;

			//keep track of the total ticks up so that the target is set correct on the next button press
			currentRackTicks = currentRackTicks + RackTicks;

		} // if

		else if(joystick.joy2_TopHat == 4 ) //Rack Down
	  {
	  	//initialize the encoder
	  	nMotorEncoder[motorHang] = 0;

	  	//set the target to go down, but only half way, we dont want to raise the robot all the way up.
			nMotorEncoderTarget[motorHang] = -(MAX_ENCODER_HANG/2);

		  //power the motor
			motor[motorHang] = -70;

			//Right and Left bumper is an emergency stop
			//wait for the encoder target to be reached, or the stop button is pressed
			while((nMotorRunState[motorHang] != runStateIdle) && (joy2Btn(6) != 1)  && (joy2Btn(5) != 1) )
			{
				//if encoder target is not met, or the emergency stop button is not pressed, continue
				getJoystickSettings(joystick);

				//read the number ticks the rack went down
				RackTicks = nMotorEncoder[motorHang];
			}
			//encoder target is reached or the emergency stop is pressed, reset the motor power to stop
			motor[motorHang] = 0;

			//keep track of the total ticks up so that the target is set correct on the next button press
			currentRackTicks = currentRackTicks + RackTicks;

	  } // else
	  else
	  	motor[motorHang] = 0;

	 // debugging purposes
	 // string sTemp;
	 // sprintf(sTemp, "%d", currentRackTicks);
	 // nxtDisplayString(2, sTemp);

	} //while true

} //Rack Task

task Rack()
{
	while(true)
	{
		getJoystickSettings(joystick);
		if (joystick.joy2_TopHat == 0 ) //Rack Up
		{
			//power the motor
			motor[motorHang] = 65;
			wait1Msec(30);
		} // if
		else if(joystick.joy2_TopHat == 4 ) //Rack Down
	  {
			motor[motorHang] = -65;
			wait1Msec(30);
	  } // else
	  else
	  	motor[motorHang] = 0;

	} //while true

} //Rack Task
/////////////////////////////////////////////////////////////////////////////////////////////////////
// scale for the drive motors
// the joy stick value should be scaled from 0 to maximum motor value
/////////////////////////////////////////////////////////////////////////////////////////////////////
int ScaleForMotor(int joyValue)
{
	const int MIN_DEAD_ZONE = -7;
	const int MAX_DEAD_ZONE = 7;
	const int MAX_MOTOR_VAL = 70;
	const float MAX_JOY_VAL = 127;

	if ( joyValue < MAX_DEAD_ZONE && joyValue > MIN_DEAD_ZONE)
		return 0;

	int direction = joyValue/abs(joyValue);
	float ratio = ((joyValue * joyValue) / (MAX_JOY_VAL * MAX_JOY_VAL));
	int Scaled = ratio * MAX_MOTOR_VAL * direction;
	return Scaled;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////
// scale for the drive motors
// the joy stick value should be scaled from 0 to maximum motor value
/////////////////////////////////////////////////////////////////////////////////////////////////////
int ScaleForMotorArm(int joyValue)
{
	const int MIN_DEAD_ZONE = -7;
	const int MAX_DEAD_ZONE = 7;
	const int MAX_MOTOR_VAL = 40; //this controls the rack and keep the power at 40;
	const float MAX_JOY_VAL = 127;

	if ( joyValue < MAX_DEAD_ZONE && joyValue > MIN_DEAD_ZONE)
		return 0;

	int direction = joyValue/abs(joyValue);
	float ratio = ((joyValue * joyValue) / (MAX_JOY_VAL * MAX_JOY_VAL));
	int Scaled = ratio * MAX_MOTOR_VAL * direction;
	return Scaled;
}

