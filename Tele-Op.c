//      *  Buttons are usually used to start/stop a motor or cause a servo to move to a specific
//         position.
//   4. Repeat the loop.
//
// Your program needs to continuously loop because you need to continuously respond to changes in
// the game controller settings.
//
// At the end of the tele-op period, the FMS will autonmatically abort (stop) execution of the program.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////

task main()
{

	initializeRobot();

	motor[motorRight] = 0;
	motor[motorLeft] = 0;
	motor[motorFlag] = 0;
	motor[motorHang] = 0;
	motor[motorArm] = 0;

	//multi tasking thread for arm
	StartTask(Arm);

	//second thread for the scoop to go higher
	StartTask(OuterRackScoop);

	//multi tasking thread for scoop
	StartTask(Scoop);

	//multi threaded task to control the rack and pinion
	StartTask(Rack);


	//main thread for drive and flag
	while (true)
	{
		//coast the motors
		bFloatDuringInactiveMotorPWM = true;

		getJoystickSettings(joystick);

		//drive controls
		if ( joystick.joy1_TopHat == 0 ) //forward
		{
				motor[motorRight] = 65;
				motor[motorLeft] = 65;
		}
		else if ( joystick.joy1_TopHat  == 4 )//backward
		{
				motor[motorRight] = -50;
				motor[motorLeft] = -50;
		}
		else if ( joystick.joy1_TopHat  == 2 )//point turn left
		{
				motor[motorLeft] = 45;
				motor[motorRight] = -45;
		}
		else if ( joystick.joy1_TopHat == 1 ) //swing right
		{
				motor[motorRight] = 20;
				motor[motorLeft] = 45;
		}
		else if ( joystick.joy1_TopHat  == 6 ) //point turn right
		{
				motor[motorRight] = 45;
				motor[motorLeft] = -45;
		}
		else if ( joystick.joy1_TopHat == 7 ) //swing left
		{
				motor[motorRight] = 45;
				motor[motorLeft] = 20;
		}
		else if (joy1Btn(2) == 1)  //move backward slowly
		{
					motor[motorRight] = -20;
					motor[motorLeft] = -20;
		}
		else if (joy1Btn(7) == 1) //ramp up, goes slowly
		{
					bFloatDuringInactiveMotorPWM = false;
					motor[motorRight] = 25;
					motor[motorLeft] = 25;
		}
		else if (joy1Btn(8) == 1) //ramp down, goes slowly
		{
					bFloatDuringInactiveMotorPWM = false;
					motor[motorRight] = -30;
					motor[motorLeft] = -30;
		}
		else
		{
			motor[motorLeft] = ScaleForMotor( joystick.joy1_y1 );
			motor[motorRight] = ScaleForMotor( joystick.joy1_y2 );
		}

		//flag control
		if  (joy2Btn(8) == 1) //right trigger spin fast
		{
				motor[motorFlag] = 90;
		}
		else if  (joy2Btn(7) == 1) //left trigger go slow
		{
				motor[motorFlag] = 35;
		}
		else
		{
				motor[motorFlag] = 0;
		}

	} //while true

} // main
