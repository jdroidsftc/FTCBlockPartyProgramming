/////////////////////////////////////////////////////////////////////////////////////////////////////
// Task: OuterRackScoop
// Allows for Arm to move further up
// Joystick 2 - controls the movement up and down
/////////////////////////////////////////////////////////////////////////////////////////////////////
task OuterRackScoop()
{
	while(true)
	{
		getJoystickSettings(joystick);
		if(joy2Btn(4) == 1)
		{
			//set the position zero to move reverse
			servo[servoOuterScoop]  = 0;
			wait1Msec(30);
		}
		else if(joy2Btn(2) == 1)
		{
			//set the position zero to move forward
			servo[servoOuterScoop]  = 255;
			wait1Msec(30);
		}
		else
		{
			//stop the continuous servo
			servo[servoOuterScoop]  = 127;
		}
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////
// Task: Scoop
// Allows for the scoop to be controlled parallely,
// servo should be scaled from 0 to 255
// the value should be increased and decreased based on the joy stick value
/////////////////////////////////////////////////////////////////////////////////////////////////////
task Scoop()
{
	const int MIN_DEAD_ZONE = -7;
	const int MAX_DEAD_ZONE = 7;
	const int MAX_SERVOVAL  = 85;
	const float MAX_JOY_VAL = 127;
	int LastValue = 0;
	while (1)
	{
		getJoystickSettings(joystick);
		int joyValue = joystick.joy2_y1;

		//Scoop PickUp: positions the scoop in the pickup position, right in the middle
		if(joy2Btn(3) == 1)
		{
			servo[servoScoop] = 0;
			wait1Msec(20);
		}
		else if(joy2Btn(1) == 1)
		{
			servo[servoScoop] = 90;
			wait1Msec(20);
		}
		//allow scoop to be controlled with the joy stick at a faster rate
		else
		{
			if ( joyValue < MAX_DEAD_ZONE && joyValue > MIN_DEAD_ZONE)
			{
					servo[servoScoop] = ServoValue[servoScoop];
			}
			else
			{
					int direction = joyValue/abs(joyValue);
					float ratio = ((joyValue * joyValue) / (MAX_JOY_VAL * MAX_JOY_VAL));
					int Scaled = ratio * MAX_SERVOVAL * direction;

					if( LastValue < Scaled &&  Scaled <= MAX_SERVOVAL  )
					{
						servo[servoScoop] = ServoValue[servoScoop]  + 2;
					}
					else
					{
						servo[servoScoop] = ServoValue[servoScoop]  - 2;
					}
			}//else joyvalue

		}// else joy2btn

	} // while true

} // task end

